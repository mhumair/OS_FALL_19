#include <stdio.h>

int main(void)
{
        printf("Hello World \n");
        printf("Print a Number      : %d \n", 15);
        printf("Print a float       : %.3f \n", 15.3333);
        printf("Print a character   : %c    \n", 'c');
        printf("Print a String      : %s    \n","Hello World");
        return 0;
}
